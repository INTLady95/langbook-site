/* MIRAVERBA — storefront i18n. The pages are written in Russian (the canonical
   source); this file overlays English/Español on the STOREFRONT UI (menus,
   home, buy). Lesson content itself stays as authored per course.

   How it works:
   - localStorage['langbook-ui-lang'] ∈ ru|en|es (ru = default, no overlay).
   - Static HTML carries data-i18n="key" (text/markup), data-i18n-ph
     (placeholder), data-i18n-aria (aria-label), data-i18n-content (meta),
     data-i18n-title (title attr). Russian stays in the markup as fallback.
   - Page scripts call T('key','русский текст') for generated strings and
     lbPlural(n,'key','день','дня','дней') for counts.
   - The dictionary LB_DICT below holds ONLY en/es. A missing key silently
     falls back to Russian — the site never breaks because of a translation.
   Load this file BEFORE theme.js and before page scripts. */
(function(){
  var KEY = 'langbook-ui-lang';
  var lang = null;
  try { lang = localStorage.getItem(KEY); } catch(e){}
  if (lang !== 'ru' && lang !== 'en' && lang !== 'es'){
    /* first visit: greet the guest in their browser's language (Tanya's
       «либо автоматически подставить»). ru-family → ru; es → es; else en.
       The guess is saved, so the switcher shows it and the user can change. */
    lang = 'en';
    try {
      var pref = (navigator.languages && navigator.languages[0]) || navigator.language || '';
      pref = String(pref).toLowerCase();
      if (/^(ru|uk|be|kk|ky|uz|tg)/.test(pref)) lang = 'ru';
      else if (/^es/.test(pref)) lang = 'es';
    } catch(e){}
    try { localStorage.setItem(KEY, lang); } catch(e){}
  }
  window.lbLang = lang;

  window.lbSetLang = function(l){
    if (l !== 'ru' && l !== 'en' && l !== 'es') return;
    if (l === lang) return;
    try { localStorage.setItem(KEY, l); } catch(e){}
    location.reload();
  };

  window.T = function(k, ru){
    if (lang === 'ru') return ru;
    var d = window.LB_DICT && window.LB_DICT[lang];
    var t = d && d[k];
    return (t == null) ? ru : t;
  };

  /* Russian needs 3 plural forms; en/es need 2. Keys: <base>.one/.many */
  window.lbPlural = function(n, base, one, few, many){
    if (lang === 'ru'){
      var m10 = n % 10, m100 = n % 100;
      return (m10 === 1 && m100 !== 11) ? one
           : (m10 >= 2 && m10 <= 4 && (m100 < 10 || m100 >= 20)) ? few : many;
    }
    return n === 1 ? T(base + '.one', one) : T(base + '.many', many);
  };

  /* localized language NAMES (subtitles); endonyms (English, Español…) are
     universal and live in the pages' data. */
  window.lbLangName = function(code){
    var RU = { ru:'Русский', en:'Английский', es:'Испанский', pt:'Португальский', de:'Немецкий', pl:'Польский', fr:'Французский' };
    return T('lang.' + code, RU[code] || code);
  };

  function applyStatic(){
    if (lang === 'ru') return;
    try { document.documentElement.setAttribute('lang', lang); } catch(e){}
    var i, el, v;
    var nodes = document.querySelectorAll('[data-i18n]');
    for (i = 0; i < nodes.length; i++){
      el = nodes[i]; v = T(el.getAttribute('data-i18n'), null);
      if (v == null) continue;
      if (v.indexOf('<') !== -1) el.innerHTML = v; else el.textContent = v;
    }
    var attrs = [ ['data-i18n-ph','placeholder'], ['data-i18n-aria','aria-label'],
                  ['data-i18n-title','title'], ['data-i18n-content','content'] ];
    for (var a = 0; a < attrs.length; a++){
      nodes = document.querySelectorAll('[' + attrs[a][0] + ']');
      for (i = 0; i < nodes.length; i++){
        el = nodes[i]; v = T(el.getAttribute(attrs[a][0]), null);
        if (v != null) el.setAttribute(attrs[a][1], v);
      }
    }
    if (document.title && lang !== 'ru'){
      var tt = T('meta.title.' + (document.body && document.body.getAttribute('data-page') || 'home'), null);
      if (tt) document.title = tt;
    }
  }
  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', applyStatic);
  else applyStatic();
})();

